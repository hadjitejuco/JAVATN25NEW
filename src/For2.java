//compute for the sum of the series of number
//compute for sum of all even and odd numbers
public class For2 {
    public static void main(String[] args) {
        int sum = 0;
        for (int i = 0; i <= 10; i++) {
            sum = sum + i;
        }
        System.out.println("The sum is "+sum);
    }
 
}
